#include<iostream>
using namespace std;

; int main()
{
    // ==  相等于
    int a = 10;
    int b = 20;
    cout << (a == b) << endl;


    //  != 不相等

    cout << (a != b) << endl;

    //  <  或  <=

    cout << (a >= b) << endl;

    //  >  或  >=

    cout << (a <= b) << endl;


    system("pause");

    return 0;

}